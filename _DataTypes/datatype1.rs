//primirive data types are int, float, bool, char

fn main() {
    let x = 5; //integer
    let y = 2.5; //float
    let z = true; //boolean
    let a = 'a'; //character


    let d: i8 = -42;
    let c: u64 = 100;

    println!("x is: {}", x);
    println!("y is: {}", y);
    println!("z is: {}", z);
    println!("a is: {}", a);
    println!("Unsigned Integer is: {}", d);
    println!("Signed Integer is: {}", c);
}